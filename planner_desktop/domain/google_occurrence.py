@dataclass(frozen=True)
class GoogleOccurrenceIdentity:
    """Exact Calendar ``originalStartTime`` identity.

    ``kind`` is ``date`` for all-day occurrences and ``datetime`` for timed
    occurrences.  Timed values retain their RFC3339 instant and the explicit
    IANA zone used by the parent series.
    """

    kind: str
    value: str
    timezone_name: Optional[str] = None

    def __post_init__(self) -> None:
        if self.kind not in ("date", "datetime"):
            raise ValueError("original start kind must be date or datetime")
        if self.kind == "date" and self.timezone_name is not None:
            raise ValueError("all-day original start must not have a timezone")
        if self.kind == "datetime" and not self.timezone_name:
            raise ValueError("timed original start requires an IANA timezone")

    @property
    def is_all_day(self) -> bool:
        return self.kind == "date"

    def to_google(self) -> dict[str, str]:
        if self.kind == "date":
            return {"date": self.value}
        return {"dateTime": self.value, "timeZone": str(self.timezone_name)}


@dataclass(frozen=True)
class OccurrenceValidationResult:
    errors: Tuple[str, ...] = field(default_factory=tuple)

    @property
    def ok(self) -> bool:
        return not self.errors

    @property
    def error(self) -> str:
        return self.errors[0] if self.errors else ""


