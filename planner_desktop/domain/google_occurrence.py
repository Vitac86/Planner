@dataclass
class OccurrenceCalendarLink:
    series_uid: str
    occurrence_key: str
    series_link_id: int
    link_generation: int
    remote_master_event_id: str
    original_start_kind: str
    original_start_value: str
    original_start_timezone: Optional[str] = None
    remote_instance_event_id: Optional[str] = None
    remote_etag: Optional[str] = None
    remote_updated_at: Optional[datetime] = None
    sync_status: OccurrenceSyncStatus = OccurrenceSyncStatus.LOCAL_ONLY
    last_synced_local_hash: Optional[str] = None
    last_synced_remote_hash: Optional[str] = None
    is_cancelled_remote: bool = False
    conflict_reason: Optional[str] = None
    conflict_snapshot_json: Optional[str] = None
    created_at: datetime = field(default_factory=utc_now)
    updated_at: datetime = field(default_factory=utc_now)
    detached_at: Optional[datetime] = None
    id: Optional[int] = None

    @property
    def identity(self) -> GoogleOccurrenceIdentity:
        return GoogleOccurrenceIdentity(
            self.original_start_kind,
            self.original_start_value,
            self.original_start_timezone,
        )

    @property
    def conflict_snapshot(self) -> Optional[dict[str, Any]]:
        if not self.conflict_snapshot_json:
            return None
        try:
            value = json.loads(self.conflict_snapshot_json)
        except ValueError:
            return None
        return value if isinstance(value, dict) else None


@dataclass
class PendingOccurrenceOperation:
    id: int
