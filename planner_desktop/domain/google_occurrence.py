"""Pure identity and state types for linked Google recurring occurrences.

An occurrence is identified by the slot generated by the *original* local
series schedule.  Its current start may move on either side without changing
that identity:

``Task.series_uid + Task.occurrence_key``
    Stable Planner identity.

``recurringEventId + originalStartTime``
    Stable Google identity.

This module deliberately has no repositories, Qt objects, Google clients or
network access.
"""
